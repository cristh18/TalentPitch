  String? about;
  String aboutVideo;
  String avatar;
  String benefitsInCompanies;
  String career;
  String city;
  dynamic compatibility;
  dynamic compatibilityReason;
  int completedProfile;
  String connected;
  String country;
  DateTime createdAt;
  String desiredRole;
  String email;
  String facebook;
  int highlight;
  int id;
  String instagram;
  String interestAreas;
  String knowledges;
  int like;
  int likesCount;
  String linkedin;
  String myLanguages;
  String name;
  String onboardingGoal;
  String position;
  String preferWork;
  String programsForProductivity;
  int requestPitch;
  String resumeDescription;
  bool resumeDownload;
  int resumeId;
  int reviewGeneralUser;
  String skills;
  String slug;
  int sponsored;
  String twitter;
  String video;
  String videoType;
  String videoUrl;
  String wantToBe;
  String wantToWork;
  String workMethodologies;
